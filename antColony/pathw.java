public class pathw {
	/* ==== ATRIBUTOS ==== */
	Integer path;
	Integer Cost;
	
	/* ==== CONSTRUTOR ==== */
	/*******************************************************
	 * 
	 * @param p
	 * @param c
	 ******************************************************/
	public pathw(Integer p, Integer c)
	{
		path=p;
		Cost=c;
	}
	
	/******************************************************
	 * 
	 * @return
	 *****************************************************/
	public Integer getPath() {
		return path;
	}
	/********************************************************
	 * 
	 * @param path
	 *******************************************************/
	public void setPath(Integer path) {
		this.path = path;
	}

	/*******************************************************
	 * 
	 * @return
	 ******************************************************/
	public Integer getCost() {
		return Cost;
	}
	/******************************************************
	 * 
	 * @param cost
	 *****************************************************/
	public void setCost(Integer cost) {
		Cost = cost;
	}
	
	/*****************************************************
	 * 
	 ****************************************************/
	public String toString() 
	{
		return " N- " + path + " C- " + Cost;
	}	

}
